package tech.asmussen.vicehel;

/**
 * This enum represents a license type.
 */
public enum LicenseType {
    /**
     * The license type A, which allows the driver to drive a motorcycle.
     */
    A,
    /**
     * The license type B, which allows the driver to drive a car.
     */
    B,
    /**
     * The license type C, which allows the driver to drive a truck.
     */
    C,
    /**
     * The license type D, which allows the driver to drive a bus.
     */
    D,
    /**
     * The license type BE, which allows the driver to drive a car with a trailer.
     */
    BE,
    /**
     * The license type CE, which allows the driver to drive a truck with a trailer.
     */
    CE,
    /**
     * The license type DE, which allows the driver to drive a bus with a trailer.
     */
    DE
}
